#可疊代的資料用於for迴圈
'''
for 變數名稱 in 可疊代的資料:
    將可疊代的資料分開
    逐一取出
'''
for x in {"a","test",10,4,11}: #集合是可疊代的資料，且無順序性，會隨機印出來
    print(x)

for i in (3,4,6):
    print(i)

for t in {"a":3,"b":4}:
    print(t)             #只取的出Key值，但印不出value

dic = {"a":3,"b":4}
for s in dic:
    print(s)
    print(dic[s]) #這樣才能把Value拿出來


