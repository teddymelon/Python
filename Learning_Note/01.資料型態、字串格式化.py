#字串的格式化：
name = "python"
age = 27

#想印出 "我是python, 今年27歲"

str = "我是" + name + ", " + "今年" + str(age) + "歲" #第一種方式，土炮。
print(str)

str = "我是{name}, 今年{age}歲".format(
    name = 'aaa', age = '29'
) #字串格式化的一種
print (str)

str = f"我是{name}, 今年{age}歲" #加上f來引用外部變數，最推薦的方式，但假如有很多，可以用上述的format。
print(str)

